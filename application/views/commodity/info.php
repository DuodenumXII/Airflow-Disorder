        <h2>运行 demo</h2>
        <ul><li>下载源码 <code>git clone git@github.com:wangfupeng1988/wangEditor.git</code></li>
            <li>安装或者升级最新版本 node（最低<code>v6.x.x</code>）</li>
            <li>进入目录，安装依赖包 <code>cd wangEditor &amp;&amp; npm i</code></li>
            <li>安装包完成之后，windows 用户运行<code>npm run win-example</code>，Mac 用户运行<code>npm run example</code></li>
            <li>打开浏览器访问<a href="http://localhost:3000/index.html">localhost:3000/index.html</a></li>
            <li>用于 React 或者 vue 可查阅<a href="http://www.kancloud.cn/wangfupeng/wangeditor3/332599">文档</a>中“其他”章节中的相关介绍</li>

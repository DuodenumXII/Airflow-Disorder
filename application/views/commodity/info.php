        <ul><li>直接下载：<a href="https://github.com/wangfupeng1988/wangEditor/releases">https://github.com/wangfupeng1988/wangEditor/releases</a></li>
            <li>使用<code>npm</code>下载：<code>npm install wangeditor</code> （注意 <code>wangeditor</code> 全部是<strong>小写字母</strong>）</li>
            <li>使用<code>bower</code>下载：<code>bower install wangEditor</code> （前提保证电脑已安装了<code>bower</code>）</li>
            <li>使用CDN：<a href="https://unpkg.com/wangeditor/release/wangEditor.min.js">//unpkg.com/wangeditor/release/wangEditor.min.js</a></li>
